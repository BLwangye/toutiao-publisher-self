export type Category = keyof typeof CATEGORY_KEYWORDS;

export function detectCategory(title: string, content?: string): Category | null {
  const text = (title + " " + (content ?? "")).toLowerCase();

  // Longest keyword wins (more specific → better match)
  let best: Category | null = null;
  let bestLen = 0;

  for (const [cat, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const kw of keywords) {
      const idx = text.indexOf(kw.toLowerCase());
      if (idx !== -1 && kw.length > bestLen) {
        best = cat as Category;
        bestLen = kw.length;
      }
    }
  }

  return best;
}

const MAX_TITLE_LENGTH = 35;

export function formatTitle(title: string, category: Category | null): string {
  // Truncate title portion only, then prepend prefix
  const truncated = truncate(title, MAX_TITLE_LENGTH);
  if (!category) return truncated;
  return `${category}> ${truncated}`;
}

function truncate(text: string, maxLen: number): string {
  const chars = [...text];
  if (chars.length <= maxLen) return text;
  return chars.slice(0, maxLen - 1).join("") + "…";
}
